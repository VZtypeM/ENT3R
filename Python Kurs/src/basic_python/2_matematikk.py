# Matematikk
a = 5 + 3
b = 5 - 3
c = 7 * 3
d = 8 / 3
e = 3**3  # eksponent

f = 11 // 3  # Heltalls divisjon (divisjon men man runder ned)
g = 11 % 3  # Modulo (Det som er igjen etter man har utført heltallsdivisjon)

print(a, b, c, d, e)
print("11 // 3 =", f)
print("11 % 3 =", g)

